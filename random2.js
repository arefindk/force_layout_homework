var node_list = function(graph){
    nodes = [];
    graph.nodes.forEach(function (node){
        nodes.push(node.id);
    });
    return nodes;
};


var adjacency_list = function(graph){
    all_nodes = node_list(graph);
    adj_list = {};
    graph.links.forEach(function (d){
        if (adj_list[d.source] === undefined){
            adj_list[d.source] = [];
            adj_list[d.source].push(d.target);
        }
        else{
            adj_list[d.source].push(d.target);
        };
        if (adj_list[d.target] === undefined){
            adj_list[d.target] = [];
            adj_list[d.target].push(d.source);
        }
        else{
            adj_list[d.target].push(d.source);
        };
    });
    all_nodes.forEach(function(node){
        if (adj_list[node] === undefined){
            adj_list[node] = [];
        };
    });
    return adj_list;
};

var non_adjacent_neighbors = function(graph){
    non_adj_nei = {};
    all_nodes = node_list(graph);
    node_ls = new Set(node_list(graph));
    adj_list = adjacency_list(graph);
    for (var k in adj_list) {
        nei = new Set(adj_list[k]);
        nei.add(k);
        minus = [...node_ls].filter(x => !nei.has(x));
        //console.log(k, "non adjacent", minus)
        non_adj_nei[k] = Array.from(minus);
        var index = all_nodes.indexOf(k);
        if (index > -1) {
            all_nodes.splice(index, 1);
        }
    };
    all_nodes.forEach(function (disconnected_node){
        non_adj_nei[disconnected_node] = Array.from(node_ls);
    });
    return non_adj_nei;
};

var distance = function (x1, y1, x2, y2){
    dist = Math.sqrt(Math.pow((x1-x2),2) + Math.pow((y1-y2),2));
    return dist;
};

var get_current_positions = function(graph){
    current_positions = {};
    graph.nodes.forEach(function (node){
        pos = {};
        pos.cx = parseFloat(d3.select("#"+node.id).attr("cx"));
        pos.cy = parseFloat(d3.select("#"+node.id).attr("cy"));
        current_positions[node.id] = pos;
    });
    return current_positions;
};

var get_theta = function(x1, y1, x2, y2){
    // first we will calculate the cos_theta
    base = Math.abs(x1 - x2);
    pythagorean_distance = distance(x1,y1,x2,y2);
    cos_theta = base / pythagorean_distance;
    theta = Math.acos(cos_theta);
    return theta;
}

var repulsion_attraction = function(c1, c2, c3, c4, graph, M){
    non_adj_nei = non_adjacent_neighbors(graph);
    adj_nei = adjacency_list(graph);
    all_nodes = node_list(graph);
    final_positions = get_current_positions(graph);
    for (iteration = 0; iteration < M; iteration++){
        position_change = {};
        all_nodes.forEach(function(current_node){
            current_a_x = 0;
            current_a_y = 0;
            current_c_x = final_positions[current_node].cx;
            current_c_y = final_positions[current_node].cy;
            
            // Now adding all the repulsions from non-adjacent neighbors
            non_adj_nei[current_node].forEach(function (nodej){
                // console.log(current_node, "gets repulsion from ", nodej);
                nei_c_x = final_positions[nodej].cx;
                nei_c_y = final_positions[nodej].cy;
                current_pythagorean_distance = distance(current_c_x, current_c_y, nei_c_x, nei_c_y);
                current_theta = get_theta(current_c_x, current_c_y, nei_c_x, nei_c_y);

                // console.log("attraction dx", d_x, "attraction dy", d_y);
                current_absolute_change_a_x = Math.abs(c3 / (Math.sqrt(current_pythagorean_distance)* Math.cos(theta)));
                current_absolute_change_a_y = Math.abs(c3 / (Math.sqrt(current_pythagorean_distance) * Math.sin(theta)));

                if ((current_c_x - nei_c_x) > 0){
                    current_a_x += current_absolute_change_a_x;
                }
                else{
                    current_a_x -= current_absolute_change_a_x;
                };
                if ((current_c_y - nei_c_y) > 0){
                    current_a_y += current_absolute_change_a_y;
                }
                else{
                    current_a_y -= current_absolute_change_a_y;   
                }
            });
            
            // now adding all x and y component of spring attractions
            adj_nei[current_node].forEach(function(nodej){
                // console.log(current_node,"gets spring attraction force", nodej);
                nei_c_x = final_positions[nodej].cx;
                nei_c_y = final_positions[nodej].cy;
                current_pythagorean_distance = distance(current_c_x, current_c_y, nei_c_x, nei_c_y);
                current_theta = get_theta(current_c_x, current_c_y, nei_c_x, nei_c_y);

                // console.log("attraction dx", d_x, "attraction dy", d_y);
                current_absolute_change_a_x = c1 * (Math.log10(current_pythagorean_distance/c2) * Math.cos(theta));
                current_absolute_change_a_y = c1 * (Math.log10(current_pythagorean_distance/c2) * Math.sin(theta));

                if ((current_c_x - nei_c_x) < 0){
                    current_a_x += current_absolute_change_a_x;
                }
                else{
                    current_a_x -= current_absolute_change_a_x;
                };
                if ((current_c_y - nei_c_y) < 0){
                    current_a_y += current_absolute_change_a_y;
                }
                else{
                    current_a_y -= current_absolute_change_a_y;   
                }
            });
            change = {};
            change.cx = c4*current_a_x;
            change.cy = c4*current_a_y;
            position_change[current_node] = change;
            // console.log("Final cx", current_c_x + (c4*current_a_x), "Final cy", current_c_y + (c4*current_a_y));
        });
        // console.log("position_change", position_change);
        // console.log("initial_position", final_positions);
        
        for (node in final_positions){
            //console.log(node, final_positions[node].cx, position_change[node].cx, final_positions[node].cx + position_change[node].cx)
            updated_x = final_positions[node].cx + position_change[node].cx
            updated_y = final_positions[node].cy + position_change[node].cy
            final_positions[node].cx = updated_x;
            final_positions[node].cy = updated_y;
        }
    }

    return_position = {}

    for (node in final_positions){
        pos = {};
        pos.cx = final_positions[node].cx; 
        pos.cy = final_positions[node].cy;
        return_position[node] = pos;
    }
    return return_position
};


